const dictionaries = {
  uk: { ...uk, ...extraUk },
  en: { ...en, ...extraEn },
  cs: { ...cs, ...extraCs },
} as const;

export function getLocale(language: "uk" | "en" | "cs") {
  if (language === "en") return "en-US";
  if (language === "cs") return "cs-CZ";
  return "uk-UA";
}

export function useI18n() {
  const language = useFinanceStore((state) => state.settings.language);
  const dictionary = dictionaries[language] ?? dictionaries.uk;

  return {
    language,
    locale: getLocale(language),
    t: (key: string) => dictionary[key] ?? dictionaries.uk[key] ?? key,
  };
}
